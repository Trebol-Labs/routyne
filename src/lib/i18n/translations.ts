type WidenTranslation<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
    ? readonly WidenTranslation<U>[]
    : T extends object
      ? { [K in keyof T]: WidenTranslation<T[K]> }
      : T;

export type TranslationTree = WidenTranslation<(typeof translationSource)['es']>;

